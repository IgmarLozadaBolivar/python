#Operadores Logicos

#And
"""
True, True = True
True, False = False
False, True = False
False, False = False
"""

#Or
"""
True, True = True
True, False = True
False, True = True
False, False = False
"""

#Not
"""
True/False
False/True
"""
a = True
b = False

print(a and b)
print(a or b)
print(not a)
print(not b)